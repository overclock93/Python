
equal = 10 == 10 # == Equal
not_equal = 10 != 10 # != Not equal
greater_than = 10 > 11 # > Greater than
less_than = 10 < 11 # < Less than
greater_equal = 10 >= 11 # >= Greater than or equal to
less_equal = 10 <= 11 # <= Less than or equal to

print('(10 == 10) = ' + str(equal))
print('(10 != 10) = ' + str(not_equal))
print('(10 > 11)  = ' + str(greater_than))
print('(10 < 11)  = ' + str(less_than))
print('(10 >= 11) = ' + str(greater_equal))
print('(10 <= 11) = ' + str(less_equal))